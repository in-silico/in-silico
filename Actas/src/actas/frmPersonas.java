    void refrescar(Personas p) {
        txtCodigo.setText(p.getCodigo());
        txtNombre.setText(p.getNombre());
        txtCorreo.setText(p.getCorreo());
        txtPrograma.setText(p.getPrograma());
        txtTalla.setText(p.getTalla());
        txtFecNac.setText(p.getFechanac().toString());
        txtFecIng.setText(p.getFechaingreso().toString());
        txtTelefonos.setText(p.getTelefonos());
        txtUniversidad.setText(p.getUniversidad());
        txtCargo.setText(p.getCargo());
    }
    
    void obtener() {        
        persona.setCodigo(txtCodigo.getText());
        persona.setNombre(txtNombre.getText());
        persona.setCorreo(txtCorreo.getText());
        persona.setPrograma(txtPrograma.getText());
        persona.setTalla(txtTalla.getText());
        persona.setFechanac(new Date(txtFecNac.getText()));
        persona.setFechaingreso(new Date(txtFecIng.getText()));
        persona.setTelefonos(txtTelefonos.getText());
        persona.setUniversidad(txtUniversidad.getText());
        persona.setCargo(txtCargo.getText());        
    }
    
